"""Render tests for templates/triage/_results.html.

Punch-list #119 (filed 2026-05-08): the template only branched on
``status == 'ok'`` and rendered every non-ok row as red "error".
This mislabelled email-triage's own outbound digest mail —
correctly marked ``status="skipped"`` by the watcher / push
consumer / runners via the X-Email-Triage header check — as
"error" rows in the operator-facing UI.

These tests render the partial directly with synthetic
``results`` rows and assert each of the three statuses produces
the right label + the right inline-detail row shape.
"""

from __future__ import annotations

import pytest

from email_triage.web.app import create_app


def _render(results: list[dict], **ctx) -> str:
    """Render templates/triage/_results.html standalone."""
    app = create_app()
    env = app.state.templates.env
    template = env.get_template("triage/_results.html")
    return template.render(
        results=results,
        errors=ctx.get("errors", []),
        total=ctx.get("total", len(results)),
        query=ctx.get("query", "test"),
        acct=ctx.get("acct", {"name": "acct1", "hipaa": False}),
        elapsed=ctx.get("elapsed", 1.0),
        hipaa_mode=ctx.get("hipaa_mode", False),
        dry_run=ctx.get("dry_run", False),
    )


class TestStatusRendering:
    """Three statuses, three render shapes."""

    def test_ok_row_renders_category_and_ok_status(self):
        html = _render([{
            "status": "ok",
            "sender": "user@example.com",
            "subject": "hello",
            "category": "newsletters",
            "confidence": 0.95,
            "actions": [],
        }])
        assert "newsletters" in html
        assert ">error<" not in html

    def test_skipped_row_renders_skipped_label_not_error(self):
        """Backend marks loop-prevention rows status='skipped'.
        Template must NOT render them as red error rows."""
        html = _render([{
            "status": "skipped",
            "sender": "Email Triage <no-reply@example.com>",
            "subject": "Your Daily Digest — placeholder",
            "reason": "self_origin",
        }])
        assert "skipped" in html
        # Status column does NOT render the red "error" label for
        # this row.
        assert "var(--pico-del-color);\">error" not in html
        # Skip-detail row carries the reason string.
        assert (
            "Skipped:" in html
            or "loop prevention" in html
            or "self_origin" in html
        )

    def test_error_row_renders_error_label_with_detail(self):
        html = _render([{
            "status": "error",
            "sender": "user@example.com",
            "subject": "errored message",
            "error": "Gmail API 404: Requested entity was not found.",
        }])
        assert ">error<" in html
        assert "Gmail API 404" in html

    def test_skipped_and_error_render_distinctly(self):
        html = _render([
            {"status": "skipped", "sender": "a@example.com",
             "subject": "loop", "reason": "self_origin"},
            {"status": "error", "sender": "b@example.com",
             "subject": "boom", "error": "boom"},
            {"status": "ok", "sender": "c@example.com",
             "subject": "fine", "category": "newsletters",
             "confidence": 0.9, "actions": []},
        ])
        assert "skipped" in html
        assert ">error<" in html
        assert "newsletters" in html

    def test_skipped_default_reason_when_missing(self):
        html = _render([{
            "status": "skipped",
            "sender": "Email Triage <no-reply@example.com>",
            "subject": "Your Daily Digest",
        }])
        assert "skipped" in html
        assert (
            "loop prevention" in html
            or "generated by email-triage" in html
        )


class TestHipaaRedaction:
    def test_skipped_row_redacts_sender_and_subject_under_hipaa(self):
        html = _render(
            [{
                "status": "skipped",
                "sender": "user@example.com",
                "subject": "private subject line",
                "reason": "self_origin",
            }],
            hipaa_mode=True,
        )
        assert "user@example.com" not in html
        assert "private subject line" not in html
        assert "[redacted]" in html
        assert "skipped" in html
